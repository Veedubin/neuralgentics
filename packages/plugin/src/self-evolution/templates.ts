/**
 * Map a pattern description to reasonable skill parameters.
 * Extracts a name, description, triggers, and workflow from the pattern text.
 */
export function patternToSkillParams(pattern: string, suggestedName: string): {
  name: string;
  description: string;
  instructions: string[];
  triggers: string[];
  workflow: string[];
  outputFormat: string;
} {
  const title = suggestedName
    .split('-')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');

  // Extract action phrases from the pattern text
  const sentences = pattern
    .split(/[.!?]+/)
    .map((s) => s.trim())
    .filter((s) => s.length > 0);

  const instructions =
    sentences.length >= 2
      ? [sentences[0], sentences[1]]
      : [`Execute the ${title} workflow`, `Report results to orchestrator`];

  const triggers = [
    `User requests ${suggestedName.replace(/-/g, ' ')} task`,
    `Pattern detected: ${pattern.slice(0, 60)}${pattern.length > 60 ? '...' : ''}`,
  ];

  const workflow =
    sentences.length >= 2
      ? [sentences[0], sentences.slice(2).join('. ') || 'Report results']
      : [`Execute ${title} workflow`, 'Report results to orchestrator'];

  return {
    name: suggestedName,
    description: `Auto-generated skill for: ${pattern.slice(0, 80)}${pattern.length > 80 ? '...' : ''}`,
    instructions,
    triggers,
    workflow,
    outputFormat: `Summary of ${title} execution with results and any follow-up actions.`,
  };
}