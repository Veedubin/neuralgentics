/**
 * Generate a SKILL.md file from a pattern candidate.
 *
 * @param name - kebab-case skill name
 * @param description - One-line description
 * @param instructions - Role description lines
 * @param triggers - When to use this skill
 * @param workflow - Workflow steps
 * @param outputFormat - Expected output description
 */
export function generateSkillMarkdown(params: {
  name: string;
  description: string;
  instructions: string[];
  triggers: string[];
  workflow: string[];
  outputFormat: string;
}): string {
  const instructionLines = params.instructions
    .map((line, i) => `${i + 1}. ${line}`)
    .join('\n');
  const triggerLines = params.triggers
    .map((t) => `- ${t}`)
    .join('\n');
  const workflowLines = params.workflow
    .map((step, i) => `### Step ${i + 1}\n${step}`)
    .join('\n\n');
  const title = params.name
    .split('-')
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');

  return `---
name: ${params.name}
description: ${params.description}
---

# ${title}

## Description
${params.description}

## Instructions

You are the **${title}** specialist. Your role is:
${instructionLines}

## Triggers

Use this skill when:
${triggerLines}

## Model

Use **MiniMax M2.7** for fast, efficient execution.

## Workflow

${workflowLines}

## Output Format

${params.outputFormat}

## Memory Integration

- Query memory for relevant context before starting work
- Save results to memory after completion
- Use trust signals (agent_used, user_confirmed) for quality feedback

## Escalation

| Situation | Escalate To |
|-----------|-------------|
| Complex design decisions | neuralgentics-architect |
| Need user confirmation | Return to orchestrator |

---

*Auto-generated by Neuralgentics Self-Evolution Gate*
`;
}

/**
 * Generate an AGENTS.md entry for a new agent.
 *
 * @param name - Agent name (kebab-case)
 * @param skill - Associated skill name
 * @param model - Recommended model
 * @param description - Agent description
 * @param justification - Why this model/agent
 */
export function generateAgentEntry(params: {
  name: string;
  skill: string;
  model: string;
  description: string;
  justification: string;
}): string {
  return `| **${params.name}** | ${params.skill} | ${params.model} | ${params.justification} |`;
}

